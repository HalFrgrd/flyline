// clap parser
// ──────────────────────────────────────────────────────────────────────────────

/// Parse clap-style `--help` output.
///
/// Typical structure:
/// ```text
/// <description>
///
/// Usage: <name> [OPTIONS] [COMMAND]
///
/// Commands:
///   subcommand1  Short description
///   subcommand2  Short description
///
/// Arguments:
///   [ARG]  description
///
/// Options:
///       --flag <VALUE>
///           Long description
///   -h, --help
///           Print help
/// ```
pub fn parse_help_clap(help: &str) -> Command {
    let lines: Vec<&str> = help.lines().collect();
    let mut cmd = Command::default();

    // Extract name from "Usage:" line; handles both "Usage: name ..." (clap/commander)
    // and "Usage:\n  name ..." (cobra/docopt) styles.
    if let Some(usage_pos) = lines
        .iter()
        .position(|l| l.trim_start().starts_with("Usage:"))
    {
        let after = lines[usage_pos].trim_start_matches(|c: char| c.is_whitespace());
        let after = after.strip_prefix("Usage:").unwrap_or(after).trim();
        if let Some(name_part) = after.split_whitespace().next() {
            // Standard "Usage: name ..." form.
            cmd.name = Some(name_part.to_string());
        } else {
            // Cobra/docopt form: "Usage:" is alone on its own line; name is on the next.
            for next_line in &lines[usage_pos + 1..] {
                let t = next_line.trim();
                if !t.is_empty() {
                    if let Some(name_part) = t.split_whitespace().next() {
                        cmd.name = Some(name_part.to_string());
                    }
                    break;
                }
            }
        }
    }

    // First non-empty line before "Usage:" is the description.
    let usage_pos = lines
        .iter()
        .position(|l| l.trim_start().starts_with("Usage:"))
        .unwrap_or(0);
    for line in &lines[..usage_pos] {
        let t = line.trim();
        if !t.is_empty() {
            cmd.description = Some(t.to_string());
            break;
        }
    }

    // Section parsing.
    let mut i = 0;
    while i < lines.len() {
        let trimmed = lines[i].trim();

        // ── Commands / Subcommands section ──────────────────────────────────
        if trimmed == "Commands:" || trimmed == "Subcommands:" || trimmed == "Available Commands:" {
            i += 1;
            while i < lines.len() {
                let line = lines[i];
                if line.trim().is_empty() {
                    i += 1;
                    break;
                }
                // Guard: a non-indented non-empty line is a new section header.
                if indent_of(line) == 0 && !line.trim().is_empty() {
                    break;
                }
                let trimmed_line = line.trim();
                // Format: "  subname  Short description"
                let mut parts = trimmed_line.splitn(2, "  ");
                let sub_name = parts.next().unwrap_or("").trim().to_string();
                let sub_desc = parts.next().map(|s| s.trim().to_string());
                if !sub_name.is_empty() {
                    cmd.subcommands.push(Command {
                        name: Some(sub_name),
                        description: sub_desc,
                        ..Command::default()
                    });
                }
                i += 1;
            }
            continue;
        }

        // ── Options / Arguments / Flags section ────────────────────────────
        if trimmed == "Options:"
            || trimmed == "Arguments:"
            || trimmed == "Flags:"
            || trimmed == "Global Flags:"
            || trimmed == "Global Options:"
        {
            i += 1;
            while i < lines.len() {
                let line = lines[i];
                if line.trim().is_empty() {
                    i += 1;
                    // blank line may separate args but we continue
                    continue;
                }
                // A non-indented non-empty line is a new section header.
                if indent_of(line) == 0 {
                    break;
                }
                let flag_indent = indent_of(line);
                let flag_part = line.trim();

                // Skip pure description continuation lines that are deeper
                // (they belong to the previous arg).
                // We identify flag lines as ones that start with '-' or '['.
                if !flag_part.starts_with('-') && !flag_part.starts_with('[') {
                    i += 1;
                    continue;
                }

                let (short, long, value_type) = parse_flag_tokens(flag_part);
                // Collect description: next lines indented more than flag_indent.
                let (desc, next_i) = collect_continuation(&lines, i + 1, flag_indent);
                i = next_i;

                let num_args = if value_type.is_some() {
                    Some("1".to_string())
                } else {
                    None
                };

                cmd.args.push(Arg {
                    short,
                    long,
                    description: if desc.is_empty() { None } else { Some(desc) },
                    value_type,
                    num_args,
                });
            }
            continue;
        }

        i += 1;
    }

    cmd
}

// ──────────────────────────────────────────────────────────────────────────────
// argparse parser
// ──────────────────────────────────────────────────────────────────────────────

/// Parse Python argparse-style `--help` output.
///
/// Typical structure:
/// ```text
/// usage: script.py [-h] [--flag VALUE] positional
///
/// Description of the program.
///
/// positional arguments:
///   positional   help text
///
/// optional arguments:
///   -h, --help   show this help message and exit
///   --flag VALUE
///                help text for flag
/// ```
pub fn parse_help_argparse(help: &str) -> Command {
    let lines: Vec<&str> = help.lines().collect();
    let mut cmd = Command::default();

    // Command name from "usage:" line.
    if let Some(usage_line) = lines
        .iter()
        .find(|l| l.trim_start().to_lowercase().starts_with("usage:"))
    {
        let after = usage_line
            .trim_start_matches(|c: char| c.is_whitespace())
            .to_lowercase();
        let after = after.strip_prefix("usage:").unwrap_or(&after).trim();
        if let Some(name) = after.split_whitespace().next() {
            cmd.name = Some(name.to_string());
        }
    }

    // Description: first non-empty line after blank line following "usage:".
    let usage_pos = lines
        .iter()
        .position(|l| l.trim_start().to_lowercase().starts_with("usage:"))
        .unwrap_or(0);
    let mut found_blank = false;
    for line in &lines[usage_pos + 1..] {
        if line.trim().is_empty() {
            found_blank = true;
        } else if found_blank {
            let t = line.trim();
            // Don't treat section headers as the description.
            if !t.ends_with(':') {
                cmd.description = Some(t.to_string());
            }
            break;
        }
    }

    // Section parsing.
    let mut i = 0;
    while i < lines.len() {
        let trimmed = lines[i].trim();

        let is_args_section = trimmed == "positional arguments:"
            || trimmed == "optional arguments:"
            || trimmed == "options:"
            || trimmed.starts_with("named arguments:");

        if is_args_section {
            i += 1;
            while i < lines.len() {
                let line = lines[i];
                if line.trim().is_empty() {
                    i += 1;
                    break;
                }
                // Non-indented line → new section.
                if indent_of(line) == 0 && line.trim().ends_with(':') {
                    break;
                }
                let flag_indent = indent_of(line);
                let flag_part = line.trim();

                // argparse flag lines start with '-' for flags or a word for positional.
                let (short, long, value_type) = if flag_part.starts_with('-') {
                    parse_flag_tokens(flag_part)
                } else {
                    // Positional argument: first word is the name.
                    let name_token = flag_part.split_whitespace().next().unwrap_or("");
                    (None, Some(name_token.to_string()), None)
                };

                // Description may be inline (after double-space) or on continuation lines.
                let inline_desc: Option<String> = if let Some(pos) = flag_part.find("  ") {
                    let d = flag_part[pos..].trim();
                    if !d.is_empty() {
                        Some(d.to_string())
                    } else {
                        None
                    }
                } else {
                    None
                };

                let (cont_desc, next_i) = collect_continuation(&lines, i + 1, flag_indent);
                i = next_i;

                let description = match (inline_desc, cont_desc.as_str()) {
                    (Some(d), "") => Some(d),
                    (None, d) if !d.is_empty() => Some(d.to_string()),
                    (Some(d), rest) => Some(format!("{} {}", d, rest)),
                    (None, _) => None,
                };

                let num_args = if value_type.is_some() {
                    Some("1".to_string())
                } else {
                    None
                };

                cmd.args.push(Arg {
                    short,
                    long,
                    description,
                    value_type,
                    num_args,
                });
            }
            continue;
        }

        i += 1;
    }

    cmd
}

// ──────────────────────────────────────────────────────────────────────────────
// Generic / fallback parser
// ──────────────────────────────────────────────────────────────────────────────

/// Fallback parser for unrecognised help formats.
///
/// Uses heuristics:
/// - Lines that start with `-` are treated as argument definitions.
/// - A two-space-separated suffix on such a line is the inline description.
/// - Deeper-indented lines following a flag are appended to its description.
pub fn parse_help_generic(help: &str) -> Command {
    let lines: Vec<&str> = help.lines().collect();
    let mut cmd = Command::default();

    // Try to find a name from the first "usage"/"Usage" line.
    for line in &lines {
        let lower = line.trim().to_lowercase();
        if lower.starts_with("usage:") {
            let after = line
                .trim()
                .trim_start_matches(|c: char| c.is_alphabetic() || c == ':')
                .trim();
            if let Some(name) = after.split_whitespace().next() {
                cmd.name = Some(name.to_string());
                break;
            }
        }
    }

    // Collect description from the first non-empty line if it doesn't look
    // like a flag or section header.
    for line in &lines {
        let t = line.trim();
        if !t.is_empty() && !t.starts_with('-') && !t.to_lowercase().starts_with("usage:") {
            cmd.description = Some(t.to_string());
            break;
        }
    }

    // Scan for argument lines.
    let mut i = 0;
    while i < lines.len() {
        let line = lines[i];
        let flag_part = line.trim();

        if flag_part.starts_with('-') {
            let flag_indent = indent_of(line);
            let (short, long, value_type) = parse_flag_tokens(flag_part);

            let inline_desc: Option<String> = if let Some(pos) = flag_part.find("  ") {
                let d = flag_part[pos..].trim();
                if !d.is_empty() {
                    Some(d.to_string())
                } else {
                    None
                }
            } else {
                None
            };

            let (cont_desc, next_i) = collect_continuation(&lines, i + 1, flag_indent);
            i = next_i;

            let description = match (inline_desc, cont_desc.as_str()) {
                (Some(d), "") => Some(d),
                (None, d) if !d.is_empty() => Some(d.to_string()),
                (Some(d), rest) => Some(format!("{} {}", d, rest)),
                (None, _) => None,
            };

            let num_args = if value_type.is_some() {
                Some("1".to_string())
            } else {
                None
            };

            cmd.args.push(Arg {
                short,
                long,
                description,
                value_type,
                num_args,
            });
            continue;
        }

        i += 1;
    }

    cmd
}

